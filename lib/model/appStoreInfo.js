var AppStoreInfo = function(appName){
    this.appName = appName;
    this.sellerName = '';
    this.artistName = this.sellerName;
    this.price = 0.0;
    this.version = '';
    this.description = '';
    this.releaseDate = undefined;
    this.trackName = '';
    this.genre = '';
    this.sellerUrl = '';
    this.appStorePage = '';
}

module.exports = exports = AppStoreInfo;

